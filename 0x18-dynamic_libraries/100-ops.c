#include <stdio.h>

/**
 * op_add - returns the sum of a and b
 * @a: first value
 * @b: second value
 * Return: sum
 */

int add(int a, int b)
{
        return (a + b);
}

/**
 * op_sub - returns the sum of a and b
 * @a: first value
 * @b: second value
 * Return: sub
 */

int sub(int a, int b)
{
        return (a - b);
}

/**
 * op_mul - returns the sum of a and b
 * @a: first value
 * @b: second value
 * Return: mul
 */

int mul(int a, int b)
{
        return (a * b);
}

/**
 * op_div - returns the sum of a and b
 * @a: first value
 * @b: second value
 * Return: div
 */

int div(int a, int b)
{
        return (a / b);
}

/**
 * op_mod - returns the sum of a and b
 * @a: first value
 * @b: second value
 * Return: mod
 */

int mod(int a, int b)
{
        return (a % b);
}
